
package DTO;


public class PacienteDTO {
    
    private int id_pacientes;
    private String nome_pacientes;
    private String sexo_pacientes;
    private String cpf_pacientes;

    public int getId_pacientes() {
        return id_pacientes;
    }

    public void setId_pacientes(int id_pacientes) {
        this.id_pacientes = id_pacientes;
    }

    public String getNome_pacientes() {
        return nome_pacientes;
    }

    public void setNome_pacientes(String nome_pacientes) {
        this.nome_pacientes = nome_pacientes;
    }

    public String getSexo_pacientes() {
        return sexo_pacientes;
    }

    public void setSexo_pacientes(String sexo_pacientes) {
        this.sexo_pacientes = sexo_pacientes;
    }

    public String getCpf_pacientes() {
        return cpf_pacientes;
    }

    public void setCpf_pacientes(String cpf_pacientes) {
        this.cpf_pacientes = cpf_pacientes;
    }
    
    
    
}
